package march3rd_Office;

import java.util.concurrent.ExecutorService;
import java.util.concurrent.Executors;
/**
 * 
 * @author Hariom Yadav | 03-Mar-2020
 *	running 1000 task parallel by using thread - expensive operation 
 *	Solution : thread pool --> executor service : upfront create 10 thread and assign 1000 task to them 
 *	internally executor service used Queue(blocking queue) to store task
 * 	threads will pick one task from QUEUE and executes it
 */
public class ThreadBasic_nThreadPool4 {
	public static void main(String[] args) {
		System.out.println("Main thread start");
		
		ExecutorService service = Executors.newFixedThreadPool(10);//create thread pool of 10 thread
		for(int i = 0; i < 1000; i++) {
			service.execute(new Task());
		}
		
		System.out.println("Main thread end ");
	}
	
	static class Task implements Runnable{
		public void run() {
			System.out.println("Thread name "+Thread.currentThread().getName());
		}
	}
}
